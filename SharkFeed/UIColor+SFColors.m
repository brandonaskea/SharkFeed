#import "UIColor+SFColors.h"
#import <UIKit/UIKit.h>

@implementation UIColor (SFColors)

+(UIColor *)darkBlue {
    return [UIColor colorWithRed:(30/255.0) green:(120/255.0) blue:(180/255.0) alpha:1];
}

+(UIColor *)lightBlue {
    return [UIColor colorWithRed:(142/255.0) green:(182/255.0) blue:(210/255.0) alpha:1];
}

@end
